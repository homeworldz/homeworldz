std::size_t visual_param_count() { return std::size(kParams); }

std::vector<std::uint8_t> build_visual_params(const std::vector<Wearable>& worn) {
    std::vector<std::uint8_t> params;
    params.reserve(std::size(kParams));
    for (const ParamDef& def : kParams) {
        float value = def.def;
        for (const Wearable& wearable : worn) {
            const auto found = wearable.parameters.find(static_cast<std::uint32_t>(def.id));
            if (found != wearable.parameters.end()) {
                value = static_cast<float>(found->second);
                break;  // first worn wearable that sets this param wins
            }
        }
        const float clamped = std::clamp(value, def.min, def.max);
        const float span = def.max - def.min;
        const float scaled = span > 0.0f ? (clamped - def.min) / span : 0.0f;
        int quantized = static_cast<int>(std::floor(scaled * 255.0f));
        quantized = std::clamp(quantized, 0, 255);
        params.push_back(static_cast<std::uint8_t>(quantized));
    }
    return params;
}

